const EN_MONTHS = ['Jan', 'Feb', 'Mar', 'Apr', 'May', 'Jun',
                   'Jul', 'Aug', 'Sep', 'Oct', 'Nov', 'Dec'];

function formatEnDate(iso: string | undefined): string | null {
  if (!iso) return null;
  const d = new Date(iso);
  if (isNaN(d.getTime())) return null;
  return `${d.getUTCDate()} ${EN_MONTHS[d.getUTCMonth()]} ${d.getUTCFullYear()}`;
}

function fallbackEnDescription(v: VersionInfo): string {
  const date = formatEnDate(v.releaseTime);
  const dateSuffix = date ? ` (${date})` : '';
  let m = /^(.+?)-rc-?(\d+)$/i.exec(v.id);
  if (m) return `Release candidate ${m[2]} for ${m[1]}${dateSuffix}.`;
  m = /^(.+?)[- ]pre[- ]?(\d+)$/i.exec(v.id) || /^(.+?) Pre-Release (\d+)$/.exec(v.id);
  if (m) return `Pre-release ${m[2]} for ${m[1]}${dateSuffix}.`;
  if (/^\d{2}w\d{2}[a-z]/i.test(v.id)) return `Snapshot ${v.id}${dateSuffix}.`;
  m = /^(\d+(?:\.\d+)?)-snapshot-(\d+)$/i.exec(v.id);
  if (m) return `Snapshot ${m[2]} of branch ${m[1]}${dateSuffix}.`;
  if (/^\d+(\.\d+){1,2}$/.test(v.id)) return `Patch ${v.id}${dateSuffix}.`;
  if (/^b\d/i.test(v.id)) return `Beta ${v.id.slice(1)}${dateSuffix}.`;
  if (/^a\d/i.test(v.id)) return `Alpha ${v.id.slice(1)}${dateSuffix}.`;
  if (/^c\d/i.test(v.id)) return `Classic ${v.id.slice(1)}${dateSuffix}.`;
  if (/^rd-/i.test(v.id)) return `Pre-Classic ${v.id}${dateSuffix}.`;
  if (/^in-/i.test(v.id)) return `Indev ${v.id.slice(3)}${dateSuffix}.`;
  if (/^inf-/i.test(v.id)) return `Infdev ${v.id.slice(4)}${dateSuffix}.`;
  return `Minecraft ${v.id}${dateSuffix}.`;
}

export function describeVersion(v: VersionInfo | undefined, locale: Locale = 'ru'): string {
  if (!v) return '';
  if (locale === 'en' || locale === 'zh' || locale === 'es' || locale === 'de') {
    return DICT_EN[v.id] ?? fallbackEnDescription(v);
  }
  return DICT[v.id] ?? `Minecraft ${v.id}.`;
}
